package com.bocai.vo;

import java.io.Serializable;

public class Token implements Serializable{

	private static final long serialVersionUID = -1145347189167560641L;
	private String id;
	private String name;
	
	public Token(String id, String name){
		this.id = id;
		this.name = name;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getId() {
		return id;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getName() {
		return name;
	}

}
