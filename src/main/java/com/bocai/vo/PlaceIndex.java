package com.bocai.vo;

import java.io.Serializable;
import java.util.LinkedList;

public class PlaceIndex implements Serializable {
	private static final long serialVersionUID = 4564906560658795701L;
	
	private String cityName;
	private String placeFullName;
	private LinkedList<Token> aggSpotList;  
	
	public PlaceIndex(String cname, String placeFname){
		this.cityName = cname;
		this.placeFullName = placeFname;
	}
	
	
	
	public String getCityName() {
		return cityName;
	}
	public void setCityName(String cityName) {
		this.cityName = cityName;
	}
	public LinkedList<Token> getAggSpotList() {
		return aggSpotList;
	}
	public void setAggSpotList(LinkedList<Token> aggSpotList) {
		this.aggSpotList = aggSpotList;
	}
	public void setPlaceFullName(String placeFullName) {
		this.placeFullName = placeFullName;
	}
	public String getPlaceFullName() {
		return placeFullName;
	}
	

}
