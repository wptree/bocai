package com.bocai.vo;

import java.io.Serializable;

@SuppressWarnings("serial")
public class PopNameCardVo implements Serializable {
	private long id;
	private String name;
	private String signature;
	private int fansNum;
	private int followingNum;
	private int sexy;
	private int totalSpotCount;
	private String cityName;
	private boolean followed;
	private String avatar;
	
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSignature() {
		return signature;
	}
	public void setSignature(String signature) {
		this.signature = signature;
	}
	public int getFansNum() {
		return fansNum;
	}
	public void setFansNum(int fansNum) {
		this.fansNum = fansNum;
	}
	public int getFollowingNum() {
		return followingNum;
	}
	public void setFollowingNum(int followingNum) {
		this.followingNum = followingNum;
	}
	public int getSexy() {
		return sexy;
	}
	public void setSexy(int sexy) {
		this.sexy = sexy;
	}
	public int getTotalSpotCount() {
		return totalSpotCount;
	}
	public void setTotalSpotCount(int totalSpotCount) {
		this.totalSpotCount = totalSpotCount;
	}
	public String getCityName() {
		return cityName;
	}
	public void setCityName(String cityName) {
		this.cityName = cityName;
	}
	public boolean isFollowed() {
		return followed;
	}
	public void setFollowed(boolean followed) {
		this.followed = followed;
	}
	public String getAvatar() {
		return avatar;
	}
	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}
	
}
